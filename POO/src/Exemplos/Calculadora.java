package Exemplos;

public class Calculadora {

	public float num1,num2;
	
	public Float adicao() {
		return num1+num2;
	}
	
	public Float subtracao() {
		return num1-num2;
	}
	
	public Float multiplicacao() {
		return num1*num2;
	}
	
	public Float divisao() {
		return num1/num2;
	}
	
	
	
}
